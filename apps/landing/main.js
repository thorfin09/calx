// Setup current language state
let currentLanguage = 'en';

function toggleLanguage() {
  currentLanguage = currentLanguage === 'en' ? 'hi' : 'en';
  document.getElementById('lang-toggle').textContent = currentLanguage === 'en' ? 'Hindi' : 'English';
  
  // Apply translation keys to all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[currentLanguage][key]) {
      el.textContent = translations[currentLanguage][key];
    }
  });
}

// Light / Dark Theme toggle logic
function toggleTheme() {
  const currentTheme = document.documentElement.getAttribute('data-theme') || 'light';
  const newTheme = currentTheme === 'light' ? 'dark' : 'light';
  
  document.documentElement.setAttribute('data-theme', newTheme);
  localStorage.setItem('theme', newTheme);
  
  updateThemeIcons(newTheme);
}

function updateThemeIcons(theme) {
  const sunIcon = document.getElementById('sun-icon');
  const moonIcon = document.getElementById('moon-icon');
  
  if (theme === 'dark') {
    sunIcon.classList.add('hidden');
    moonIcon.classList.remove('hidden');
  } else {
    sunIcon.classList.remove('hidden');
    moonIcon.classList.add('hidden');
  }
}

// APK Download Modal logic
const apkBtn = document.getElementById('download-apk-btn');
const modal = document.getElementById('apk-modal');
const closeModalBtn = document.getElementById('close-modal-btn');

function openModal() {
  modal.classList.remove('hidden');
}

function closeModal() {
  modal.classList.add('hidden');
}

// Event Listeners
document.getElementById('lang-toggle').addEventListener('click', toggleLanguage);
document.getElementById('theme-toggle').addEventListener('click', toggleTheme);

if (apkBtn) apkBtn.addEventListener('click', openModal);
if (closeModalBtn) closeModalBtn.addEventListener('click', closeModal);

// Initialize theme from localStorage on load
const savedTheme = localStorage.getItem('theme') || 'light';
document.documentElement.setAttribute('data-theme', savedTheme);
updateThemeIcons(savedTheme);
closeModal(); // Ensure modal is closed
